function showNormal() {
  document.getElementById("normal").show();
}

function showHorizontal() {
  document.getElementById("horizontal").show();
}

function closeNormal() {
  document.getElementById("normal").close();
}

function closeHorizontal() {
  document.getElementById("horizontal").close();
}

function toggleTheme() {
  document.body.classList.toggle("light");
}
